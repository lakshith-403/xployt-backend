package com.xployt.model;

public class ProjectConfig {
  private String projectId;
  private String testingScope;
  private String outOfScope;
  private String objectives;
  private String securityRequirements;
  private String critical;
  private String high;
  private String medium;
  private String low;
  private String informative;
  private String visibility;
  private String attachments;
  private String initialFunding;

  // Getters and Setters
  public String getProjectId() {
    return projectId;
  }

  public void setProjectId(String projectId) {
    this.projectId = projectId;
  }

  public String getTestingScope() {
    return testingScope;
  }

  public void setTestingScope(String testingScope) {
    this.testingScope = testingScope;
  }

  public String getOutOfScope() {
    return outOfScope;
  }

  public void setOutOfScope(String outOfScope) {
    this.outOfScope = outOfScope;
  }

  public String getObjectives() {
    return objectives;
  }

  public void setObjectives(String objectives) {
    this.objectives = objectives;
  }

  public String getSecurityRequirements() {
    return securityRequirements;
  }

  public void setSecurityRequirements(String securityRequirements) {
    this.securityRequirements = securityRequirements;
  }

  public String getCritical() {
    return critical;
  }

  public void setCritical(String critical) {
    this.critical = critical;
  }

  public String getHigh() {
    return high;
  }

  public void setHigh(String high) {
    this.high = high;
  }

  public String getMedium() {
    return medium;
  }

  public void setMedium(String medium) {
    this.medium = medium;
  }

  public String getLow() {
    return low;
  }

  public void setLow(String low) {
    this.low = low;
  }

  public String getInformative() {
    return informative;
  }

  public void setInformative(String informative) {
    this.informative = informative;
  }

  public String getVisibility() {
    return visibility;
  }

  public void setVisibility(String visibility) {
    this.visibility = visibility;
  }

  public String getAttachments() {
    return attachments;
  }

  public void setAttachments(String attachments) {
    this.attachments = attachments;
  }

  public String getInitialFunding() {
    return initialFunding;
  }

  public void setInitialFunding(String initialFunding) {
    this.initialFunding = initialFunding;
  }
}